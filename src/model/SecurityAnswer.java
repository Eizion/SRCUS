package model;

public class SecurityAnswer {
	
	private int an_id;
	private int sq_id;
	private int user_id;
	private String answer;
	/**
	 * @return the an_id
	 */
	public int getAn_id() {
		return an_id;
	}
	/**
	 * @param an_id the an_id to set
	 */
	public void setAn_id(int an_id) {
		this.an_id = an_id;
	}
	/**
	 * @return the sq_id
	 */
	public int getSq_id() {
		return sq_id;
	}
	/**
	 * @param sq_id the sq_id to set
	 */
	public void setSq_id(int sq_id) {
		this.sq_id = sq_id;
	}
	/**
	 * @return the user_id
	 */
	public int getUser_id() {
		return user_id;
	}
	/**
	 * @param user_id the user_id to set
	 */
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	/**
	 * @return the answer
	 */
	public String getAnswer() {
		return answer;
	}
	/**
	 * @param answer the answer to set
	 */
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	
	

}
