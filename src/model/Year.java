package model;

public class Year {

	private int year;
	
	public Year(){
		
	}
	
	public Year(int year){
		this.year = year;
	}

	/**
	 * @return the year
	 */
	public int getYear() {
		return year;
	}

	/**
	 * @param year the year to set
	 */
	public void setYear(int year) {
		this.year = year;
	}
	
	
	
}
